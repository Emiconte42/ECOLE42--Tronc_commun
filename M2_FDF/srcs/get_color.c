#include "../includes/fdf.h"

/*****************************************************************************
				        RETURN COLOR ABOUT ALTITUDE
*****************************************************************************/

/*===========================================
Color initialisation
===========================================*/
int	get_color(int z, t_fdf *fdf)
{
	double	t;
	int		r;
	int		g;
	int		b;
	int		color;	

	if (fdf->map.z_max == fdf->map.z_min)
	{
		color = 11920639;
		return (color);
	}
	t = (double)(z - fdf->map.z_min) / (fdf->map.z_max - fdf->map.z_min);
	r = (int)(181 + t * (255 - 181));
	g = (int)(228 + t * (74 - 228));
	b = (int)(255 + t * (74 - 255));
	return (r * 65536 + g * 256 + b);
}
